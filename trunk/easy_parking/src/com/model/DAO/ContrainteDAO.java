package com.model.DAO;

public class ContrainteDAO {

	private String typeContrainte;
	private String detailContrainte;
	
	public ContrainteDAO() {
		// no args
	}
	
	public String getTypeContrainte() {
		return typeContrainte;
	}
	public void setTypeContrainte(String typeContrainte) {
		this.typeContrainte = typeContrainte;
	}
	public String getDetailContrainte() {
		return detailContrainte;
	}
	public void setDetailContrainte(String detailContrainte) {
		this.detailContrainte = detailContrainte;
	}
}
